function printScan(scanType, result, notes, imageUrl, patientName, nationalId) {
    var win = window.open('', '_blank');
    var imgContent = '';

    if (imageUrl) {
        if (imageUrl.indexOf('.pdf') !== -1) {
            imgContent = '<a href="' + imageUrl + '" style="display:inline-block;padding:12px 24px;background:#0369a1;color:white;border-radius:10px;text-decoration:none;font-weight:700;">Open PDF Report</a>';
        } else {
            imgContent = '<img src="' + imageUrl + '" style="max-width:100%;border-radius:12px;margin-top:16px;" />';
        }
    }

    win.document.write(
        '<html><head><title>Radiology Report - HealthMSR</title>' +
        '<style>' +
        'body{font-family:sans-serif;padding:40px;color:#0f172a;max-width:800px;margin:auto;}' +
        'h1{color:#0369a1;font-size:1.8rem;margin-bottom:8px;}' +
        '.label{font-size:0.75rem;font-weight:700;color:#64748b;text-transform:uppercase;letter-spacing:1px;margin-top:20px;display:block;}' +
        '.value{font-size:1.1rem;font-weight:600;margin-top:4px;display:block;}' +
        'hr{border:1px solid #e2e8f0;margin:20px 0;}' +
        '.header{display:flex;justify-content:space-between;align-items:center;margin-bottom:20px;}' +
        '.badge{background:#e0f2fe;color:#0369a1;padding:6px 16px;border-radius:20px;font-size:0.85rem;font-weight:700;}' +
        '</style></head><body>' +
        '<div class="header">' +
        '<h1>HealthMSR</h1>' +
        '<span class="badge">Radiology Report</span>' +
        '</div>' +
        '<hr/>' +
        '<span class="label">Patient Name</span>' +
        '<span class="value">' + patientName + '</span>' +
        '<span class="label">National ID</span>' +
        '<span class="value">' + nationalId + '</span>' +
        '<span class="label">Scan Type</span>' +
        '<span class="value">' + scanType + '</span>' +
        '<span class="label">Result</span>' +
        '<span class="value">' + (result || 'No result yet') + '</span>' +
        '<span class="label">Notes</span>' +
        '<span class="value">' + (notes || '—') + '</span>' +
        '<hr/>' +
        imgContent +
        '<hr style="margin-top:40px;"/>' +
        '<p style="font-size:0.8rem;color:#94a3b8;text-align:center;">Generated by HealthMSR System</p>' +
        '</body></html>'
    );
    win.document.close();
    win.print();
}

function validateNID(input) {
    input.value = input.value.replace(/\D/g, '');
    var error = document.getElementById('nidError');
    if (!error) return;
    if (input.value.length > 0 && input.value.length < 14) {
        error.style.display = 'block';
        error.textContent = 'National ID must be exactly 14 digits (' + input.value.length + '/14)';
    } else {
        error.style.display = 'none';
    }
}

function checkNID() {
    var input = document.getElementById('searchInput');
    var error = document.getElementById('nidError');
    if (!input) return true;
    if (input.value.length !== 14) {
        if (error) {
            error.style.display = 'block';
            error.textContent = 'Please enter a complete 14-digit National ID';
        }
        input.focus();
        return false;
    }
    return true;
}

function showTab(tabName) {
    document.querySelectorAll('.tab-content').forEach(function (el) {
        el.style.display = 'none';
    });
    document.querySelectorAll('.tab-btn').forEach(function (el) {
        el.style.background = 'var(--white)';
        el.style.color = 'var(--gray)';
        el.style.borderColor = 'var(--border)';
    });
    var content = document.getElementById('content-' + tabName);
    if (content) content.style.display = 'block';
    var btn = document.getElementById('tab-' + tabName);
    if (btn) {
        btn.style.background = 'var(--teal)';
        btn.style.color = 'white';
        btn.style.borderColor = 'var(--teal)';
    }
}

function addMedRow() {
    var list = document.getElementById('med-list');
    if (!list) return;
    var row = list.querySelector('.med-row').cloneNode(true);
    row.querySelectorAll('input').forEach(function (i) { i.value = ''; });
    list.appendChild(row);
}

function toggleFaq(btn) {
    var answer = btn.nextElementSibling;
    var icon = btn.querySelector('.faq-icon');
    var isOpen = answer.style.display === 'block';
    answer.style.display = isOpen ? 'none' : 'block';
    if (icon) {
        icon.textContent = isOpen ? '+' : 'x';
        icon.style.transform = isOpen ? 'rotate(0deg)' : 'rotate(45deg)';
    }
}

function toggleLang() {
    var app = document.getElementById('app');
    if (!app) return;
    var isArabic = app.getAttribute('dir') === 'rtl';
    app.setAttribute('dir', isArabic ? 'ltr' : 'rtl');
    var btn = document.getElementById('langBtn');
    if (btn) btn.textContent = isArabic ? 'عربي' : 'English';
    document.querySelectorAll('[data-en]').forEach(function (el) {
        el.textContent = isArabic ? el.getAttribute('data-en') : el.getAttribute('data-ar');
    });
}

// Dark Mode
document.addEventListener('DOMContentLoaded', function () {
    if (localStorage.getItem('darkMode') === 'true') {
        document.body.classList.add('dark-mode');
        var btn = document.getElementById('darkToggle');
        if (btn) btn.textContent = 'sun';
    }
});

function toggleDark() {
    document.body.classList.toggle('dark-mode');
    var isDark = document.body.classList.contains('dark-mode');
    var btn = document.getElementById('darkToggle');
    if (btn) btn.textContent = isDark ? 'sun' : 'moon';
    localStorage.setItem('darkMode', isDark);
}