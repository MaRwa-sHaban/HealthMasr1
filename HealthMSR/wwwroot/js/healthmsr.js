function printScan(scanType, result, notes, imageUrl, patientName, nationalId) {
    var win = window.open('', '_blank');
    if (!win) return;

    var imgContent = '';
    if (imageUrl && imageUrl !== '') {
        if (imageUrl.toLowerCase().indexOf('.pdf') !== -1) {
            imgContent = '<div style="margin-top:24px;"><a href="' + imageUrl + '" target="_blank" style="display:inline-block;padding:12px 24px;background:#0369a1;color:white;border-radius:10px;text-decoration:none;font-weight:700;font-family:sans-serif;">Open PDF Report</a></div>';
        } else {
            imgContent = '<div style="margin-top:24px;"><img src="' + imageUrl + '" style="max-width:100%;border-radius:12px;border:1px solid #e2e8f0;" /></div>';
        }
    }

    var html = '<!DOCTYPE html><html><head><title>Radiology Report - HealthMSR</title>';
    html += '<style>';
    html += 'body{font-family:sans-serif;padding:40px;color:#0f172a;max-width:800px;margin:auto;}';
    html += 'h1{color:#0369a1;font-size:1.8rem;margin-bottom:4px;}';
    html += '.subtitle{color:#64748b;font-size:0.9rem;margin-bottom:20px;}';
    html += 'hr{border:none;border-top:1px solid #e2e8f0;margin:20px 0;}';
    html += '.label{font-size:0.72rem;font-weight:700;color:#64748b;text-transform:uppercase;letter-spacing:1px;margin-bottom:4px;display:block;}';
    html += '.value{font-size:1.1rem;font-weight:600;color:#0f172a;display:block;margin-bottom:16px;}';
    html += '.badge{display:inline-block;background:#e0f2fe;color:#0369a1;padding:4px 14px;border-radius:20px;font-size:0.82rem;font-weight:700;}';
    html += '.header{display:flex;justify-content:space-between;align-items:flex-start;}';
    html += '.footer{margin-top:40px;padding-top:16px;border-top:1px solid #e2e8f0;font-size:0.78rem;color:#94a3b8;text-align:center;}';
    html += '</style></head><body>';

    html += '<div class="header">';
    html += '<div><h1>HealthMSR</h1><div class="subtitle">Electronic Health Records System</div></div>';
    html += '<span class="badge">Radiology Report</span>';
    html += '</div>';
    html += '<hr/>';

    html += '<span class="label">Patient Name</span>';
    html += '<span class="value">' + patientName + '</span>';

    html += '<span class="label">National ID</span>';
    html += '<span class="value">' + nationalId + '</span>';

    html += '<hr/>';

    html += '<span class="label">Scan Type</span>';
    html += '<span class="value">' + scanType + '</span>';

    html += '<span class="label">Result</span>';
    html += '<span class="value">' + (result || 'No result recorded') + '</span>';

    if (notes && notes !== '' && notes !== 'null') {
        html += '<span class="label">Notes</span>';
        html += '<span class="value">' + notes + '</span>';
    }

    html += imgContent;

    html += '<div class="footer">Generated by HealthMSR System &mdash; ' + new Date().toLocaleDateString() + '</div>';
    html += '</body></html>';

    win.document.write(html);
    win.document.close();
    setTimeout(function () { win.print(); }, 500);
}
function validateNID(input) {
    input.value = input.value.replace(/\D/g, '');
    var error = document.getElementById('nidError');
    if (!error) return;
    if (input.value.length > 0 && input.value.length < 14) {
        error.style.display = 'block';
        error.textContent = 'National ID must be exactly 14 digits (' + input.value.length + '/14)';
    } else {
        error.style.display = 'none';
    }
}

function checkNID() {
    var input = document.getElementById('searchInput');
    var error = document.getElementById('nidError');
    if (!input) return true;
    if (input.value.length !== 14) {
        if (error) {
            error.style.display = 'block';
            error.textContent = 'Please enter a complete 14-digit National ID';
        }
        input.focus();
        return false;
    }
    return true;
}

function showTab(tabName) {
    document.querySelectorAll('.tab-content').forEach(function (el) {
        el.style.display = 'none';
    });
    document.querySelectorAll('.tab-btn').forEach(function (el) {
        el.style.background = 'var(--white)';
        el.style.color = 'var(--gray)';
        el.style.borderColor = 'var(--border)';
    });
    var content = document.getElementById('content-' + tabName);
    if (content) content.style.display = 'block';
    var btn = document.getElementById('tab-' + tabName);
    if (btn) {
        btn.style.background = 'var(--teal)';
        btn.style.color = 'white';
        btn.style.borderColor = 'var(--teal)';
    }
}

function addMedRow() {
    var list = document.getElementById('med-list');
    if (!list) return;
    var row = list.querySelector('.med-row').cloneNode(true);
    row.querySelectorAll('input').forEach(function (i) { i.value = ''; });
    list.appendChild(row);
}

function toggleFaq(btn) {
    var answer = btn.nextElementSibling;
    var icon = btn.querySelector('.faq-icon');
    var isOpen = answer.style.display === 'block';
    answer.style.display = isOpen ? 'none' : 'block';
    if (icon) {
        icon.textContent = isOpen ? '+' : 'x';
        icon.style.transform = isOpen ? 'rotate(0deg)' : 'rotate(45deg)';
    }
}

function toggleLang() {
    var app = document.getElementById('app');
    if (!app) return;
    var isArabic = app.getAttribute('dir') === 'rtl';
    app.setAttribute('dir', isArabic ? 'ltr' : 'rtl');
    var btn = document.getElementById('langBtn');
    if (btn) btn.textContent = isArabic ? 'عربي' : 'English';
    document.querySelectorAll('[data-en]').forEach(function (el) {
        el.textContent = isArabic ? el.getAttribute('data-en') : el.getAttribute('data-ar');
    });
}

// Dark Mode
document.addEventListener('DOMContentLoaded', function () {
    if (localStorage.getItem('darkMode') === 'true') {
        document.body.classList.add('dark-mode');
        var btn = document.getElementById('darkToggle');
        if (btn) btn.textContent = 'sun';
    }
});

function toggleDark() {
    document.body.classList.toggle('dark-mode');
    var isDark = document.body.classList.contains('dark-mode');
    var btn = document.getElementById('darkToggle');
    if (btn) btn.textContent = isDark ? '☀️' : '🌙';
    localStorage.setItem('darkMode', isDark);
}

